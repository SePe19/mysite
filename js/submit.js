function loginSubmit(event) {
    if (event.key === "Enter") {
        login()
    }
}

function signupSubmit(event) {
    if (event.key === "Enter") {
        signUp()
    }
}

function resetSubmit(event) {
    if (event.key === "Enter") {
        resetPassword()
    }
}

function tweetSubmit(event) {
    if (event.key === "Enter") {
        tweet()
    }
}
